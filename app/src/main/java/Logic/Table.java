package Logic;


public class Table {

    private int number;
    private int numOfSeats;

    public Table(int number, int numOfSeats) {
        this.number = number;
        this.numOfSeats = numOfSeats;
    }

    public int getNumber() {
        return number;
    }

    public int getNumOfSeats() {
        return numOfSeats;
    }
}
